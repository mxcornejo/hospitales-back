-- Tabla de usuarios (para el BFF / Spring Security)
CREATE TABLE USUARIOS (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME    VARCHAR2(100) UNIQUE NOT NULL,
    PASSWORD    VARCHAR2(255) NOT NULL,
    ROL         VARCHAR2(50)  DEFAULT 'USER',
    ACTIVO      NUMBER(1)     DEFAULT 1
);

-- Tabla de pacientes
CREATE TABLE PACIENTES (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE        VARCHAR2(100)  NOT NULL,
    APELLIDO      VARCHAR2(100)  NOT NULL,
    RUT           VARCHAR2(20)   UNIQUE NOT NULL,
    EDAD          NUMBER         NOT NULL,
    HABITACION    VARCHAR2(20),
    DIAGNOSTICO   VARCHAR2(500),
    ESTADO        VARCHAR2(20)   DEFAULT 'ESTABLE',
    FECHA_INGRESO TIMESTAMP      DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de signos vitales
CREATE TABLE SIGNOS_VITALES (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PACIENTE_ID         NUMBER NOT NULL,
    FRECUENCIA_CARDIACA NUMBER,
    PRESION_SISTOLICA   NUMBER,
    PRESION_DIASTOLICA  NUMBER,
    SATURACION_OXIGENO  NUMBER(5,2),
    TEMPERATURA         NUMBER(5,1),
    FECHA_HORA          TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de alertas
CREATE TABLE ALERTAS (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENTO_ID    VARCHAR2(36) UNIQUE,
    PACIENTE_ID  NUMBER       NOT NULL,
    TIPO         VARCHAR2(50) NOT NULL,
    SEVERIDAD    VARCHAR2(20) NOT NULL,
    DESCRIPCION  VARCHAR2(500),
    ESTADO       VARCHAR2(20) DEFAULT 'ACTIVA',
    FECHA_HORA   TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);

